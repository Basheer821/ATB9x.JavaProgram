package Oct.ex_21102024_Forloop;

public class Lab_87 {
    public static void main(String[] args) {

        for(int _i=1;_i<=10;_i++){
            System.out.println(_i);
        }

        //int _i-->0-I
        //_i<=10-->condition
        //_i++ updation

        for (int i=0;i<10;++i){
            System.out.println(i);
        }
    }
}
