package Oct.ex_18102024_ifcondition_switch;

public class Lab_83 {
    public static void main(String[] args) {
        char code='C';
        switch(code){
            default -> System.out.println("hellooooo");
            case 'A' -> System.out.println("asdfg");
            case 'B' -> System.out.println("qwerty");
            case 'C' -> System.out.println("zxcvb");
            case 'D' -> System.out.println("qwertyu");
        }
    }
}
